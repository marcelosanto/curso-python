# Manipulando Strings

# indices positivos -> 'Python S2' = [012345678]
texto = 'Python S2'
# indices negativos -> 'Python S2' = -[987654321]

print(texto[5])
print(texto[-5])

# indices positivos
nova_string = texto[:6]
print(nova_string)

# indices negativos
nova_string_2 = texto[-9:-3]
print(nova_string_2)
